//  Crear Funciones en JS - Function expression y Function Declaration

// Declaración  de Función { Fuction Declaration }
function sumar() {
    console.log (2 + 2);
}

sumar();
sumar();
sumar();


// Las dos funciones son exactamente igual
// La diferencia es que una se parece a crear variables

// Expresión de Fuction (Function Expression)
const sumar2 = function() {
    console.log( 3 +3 );

}

sumar2();